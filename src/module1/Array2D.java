package module1;
import java.util.Arrays;
public class Array2D {
	public static void main(String[] args) {
		int[][]finalMarks= {{60,21,39},{90,44,1}};
		System.out.println(finalMarks[0][0]);//60
		System.out.println(finalMarks[0][3]);//30
		System.out.println(finalMarks[1][1]);//90
		System.out.println(finalMarks[1][3]);//100
	}

}
